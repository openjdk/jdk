/*
 * NASHORN-658: nasgen generated constructor functions all have function id 0.
 *
 * @test
 * @run
 */

function C1() {}
function C2() {}
function C3() {}

var ctors = [C1,C2,Number,String,Date,C3];

(function() {
for (var i in ctors) {
  var c = ctors[i];
  if (new c().constructor !== c) {
    throw "Error: new c().constructor !== c";
  }
}
})();

