package jdk.internal.event;

import java.lang.reflect.Field;

public class FinalFieldMutationEvent extends Event {

    public Class<?> declaringClass;
    public String fieldName;

    /**
     * We throttle FinalFieldMutation JFR events by generating only one event per field
     * during each JFR epoch.
     *
     * An event is emitted if enabled, the epoch encoded in the root instance differs
     * from the current epoch, and a thread exclusively updates to the current epoch.
     */
    public static void offer(Field root) {
        if (enabled() && tryUpdateEpoch(root)) {
            var event = new FinalFieldMutationEvent();
            event.declaringClass = root.getDeclaringClass();
            event.fieldName = root.getName();
            event.commit();
        }
    }

    private static boolean tryUpdateEpoch(Field root) {
        // Generated by JFR
        return false;
    }

    public static boolean enabled() {
        // Generated by JFR
        return false;
    }
}
