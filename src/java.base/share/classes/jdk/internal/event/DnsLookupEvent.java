package jdk.internal.event;

import java.net.InetAddress;

/**
 * A JFR event for DNS lookup operations.  This event is mirrored in
 * {@code jdk.jfr.events.DnsLookupEvent } where the metadata for the event is
 * provided with annotations.  Some of the methods are replaced by generated
 * methods when jfr is enabled.  Note that the order of the arguments of the
 * {@link #commit(long, long, String, String, boolean, boolean, long, boolean)} method
 * must be the same as the order of the fields.
 */
public class DnsLookupEvent extends Event {

    // THE ORDER OF THE FOLLOWING FIELDS IS IMPORTANT!
    // The order must match the argument order of the generated commit method.
    public String host;
    public String result;
    public boolean success;
    public boolean cached;
    public long ttl;
    public boolean stale;

    /**
     * Actually commit a DNS lookup event.  The implementation
     * of this method is generated automatically if jfr is enabled.
     * The order of the fields must be the same as the parameters in this method.
     * {@code commit(..., String, String, boolean, boolean, long, boolean)}
     *
     * @param start     timestamp of the start of the operation
     * @param duration  time in nanoseconds to complete the operation
     * @param host      hostname being looked up
     * @param result    comma-separated list of resolved IP addresses, or error message
     * @param success   true if lookup was successful, false otherwise
     * @param cached    true if result was retrieved from cache, false if actual DNS query was performed
     * @param ttl       time to live in seconds (0 if cached=false, -1 if forever, actual TTL if cached=true)
     * @param stale     true if stale cached data was used (only valid when cached=true)
     */
    public static void commit(long start, long duration, String host, String result, boolean success, boolean cached, long ttl, boolean stale) {
        // Generated by JFR
    }

    /**
     * Determine if an event should be emitted.  The duration of the operation
     * must exceed some threshold in order to commit the event.  The implementation
     * of this method is generated automatically if jfr is enabled.
     *
     * @param duration  time in nanoseconds to complete the operation
     * @param end  timestamp at the end of the operation
     * @return  true if the event should be committed
     */
    public static boolean shouldThrottleCommit(long duration, long end) {
        // Generated by JFR
        return false;
    }

    /**
     * Determine if this kind of event is enabled.  The implementation
     * of this method is generated automatically if jfr is enabled.
     *
     * @return true if DNS lookup events are enabled, false otherwise
     */
    public static boolean enabled() {
        // Generated by JFR
        return false;
    }

    /**
     * Fetch the current timestamp in nanoseconds.  This method is used
     * to determine the start and end of an operation.  The implementation
     * of this method is generated automatically if jfr is enabled.
     *
     * @return  the current timestamp value
     */
    public static long timestamp() {
        // Generated by JFR
        return 0L;
    }

    /**
     * Helper method to offer the data needed to potentially commit an event
     * for actual DNS network query.
     *
     * @param start  the start time
     * @param host   hostname being looked up
     * @param addresses  resolved IP addresses (null if lookup failed)
     * @param success true if lookup was successful, false otherwise
     */
    public static void offerNetworkQuery(long start, String host, InetAddress[] addresses, boolean success) {
        long end = timestamp();
        long duration = end - start;
        if (shouldThrottleCommit(duration, end)) {
            emitNetworkQuery(start, duration, host, addresses, success);
        }
    }

    /**
     * Helper method to offer the data needed to potentially commit an event
     * for actual DNS network query that failed.
     *
     * @param start  the start time
     * @param host   hostname being looked up
     * @param errorMessage  error message from the failed lookup
     */
    public static void offerNetworkQuery(long start, String host, String errorMessage) {
        long end = timestamp();
        long duration = end - start;
        if (shouldThrottleCommit(duration, end)) {
            emitNetworkQuery(start, duration, host, errorMessage, false);
        }
    }

    /**
     * Helper method to offer the data needed to potentially commit an event
     * for cached lookup result.
     *
     * @param start  the start time
     * @param host   hostname being looked up
     * @param addresses  resolved IP addresses (null if cached error)
     * @param success true if lookup was successful, false if cached error
     * @param ttl    time to live in seconds (0 if forever, actual TTL otherwise)
     * @param stale  true if stale cached data was used
     */
    public static void offerCachedResult(long start, String host, InetAddress[] addresses, boolean success, long ttl, boolean stale) {
        long end = timestamp();
        long duration = end - start;
        if (shouldThrottleCommit(duration, end)) {
            emitCachedResult(start, duration, host, addresses, success, ttl, stale);
        }
    }

    /**
     * Helper method to offer the data needed to potentially commit an event
     * for cached error result.
     *
     * @param start  the start time
     * @param host   hostname being looked up
     * @param errorMessage  error message from the cached failed lookup
     * @param ttl    time to live in seconds for negative cache
     */
    public static void offerCachedError(long start, String host, String errorMessage, long ttl) {
        long end = timestamp();
        long duration = end - start;
        if (shouldThrottleCommit(duration, end)) {
            emitCachedError(start, duration, host, errorMessage, ttl);
        }
    }

    /**
     * Helper method to perform a common task of getting event data ready and
     * then emitting the event for actual DNS network query.
     *
     * @param start  the start time
     * @param duration the duration
     * @param host   hostname being looked up
     * @param addresses  resolved IP addresses (null if lookup failed)
     * @param success true if lookup was successful, false otherwise
     */
    public static void emitNetworkQuery(long start, long duration, String host, InetAddress[] addresses, boolean success) {
        String result;
        if (addresses != null && addresses.length > 0) {
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < addresses.length; i++) {
                if (i > 0) {
                    sb.append(", ");
                }
                sb.append(addresses[i].getHostAddress());
            }
            result = sb.toString();
        } else {
            result = "UnknownHostException";
        }
        commit(start, duration, host, result, success, false, 0, false);
    }

    /**
     * Helper method to perform a common task of getting event data ready and
     * then emitting the event for actual DNS network query that failed.
     *
     * @param start  the start time
     * @param duration the duration
     * @param host   hostname being looked up
     * @param errorMessage  error message from the failed lookup
     * @param success false (always false for failed lookups)
     */
    public static void emitNetworkQuery(long start, long duration, String host, String errorMessage, boolean success) {
        commit(start, duration, host, errorMessage, success, false, 0, false);
    }

    /**
     * Helper method to perform a common task of getting event data ready and
     * then emitting the event for cached lookup result.
     *
     * @param start  the start time
     * @param duration the duration
     * @param host   hostname being looked up
     * @param addresses  resolved IP addresses (null if cached error)
     * @param success true if lookup was successful, false if cached error
     * @param ttl    time to live in seconds (0 if forever, actual TTL otherwise)
     * @param stale  true if stale cached data was used
     */
    public static void emitCachedResult(long start, long duration, String host, InetAddress[] addresses, boolean success, long ttl, boolean stale) {
        String result;
        if (addresses != null && addresses.length > 0) {
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < addresses.length; i++) {
                if (i > 0) {
                    sb.append(", ");
                }
                sb.append(addresses[i].getHostAddress());
            }
            result = sb.toString();
        } else {
            result = "UnknownHostException (cached)";
        }
        commit(start, duration, host, result, success, true, ttl, stale);
    }

    /**
     * Helper method to perform a common task of getting event data ready and
     * then emitting the event for cached error result.
     *
     * @param start  the start time
     * @param duration the duration
     * @param host   hostname being looked up
     * @param errorMessage  error message from the cached failed lookup
     * @param ttl    time to live in seconds for negative cache
     */
    public static void emitCachedError(long start, long duration, String host, String errorMessage, long ttl) {
        commit(start, duration, host, errorMessage, false, true, ttl, false);
    }
}
